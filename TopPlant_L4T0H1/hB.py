from numpy import *

def hB(x,a,u,d,p,nn):
    x1=x[0] 
    x2=x[1] 
    x3=x[2] 
    x4=x[3] 
    x5=x[4] 
    x6=x[5] 
    x7=x[6] 
    u2=u[1] 
    u11=u[10] 
    u17=u[16] 
    u23=u[22] 
    u29=u[28] 
    d1=d[0] 
    d2=d[1] 
    p2=p[1] 
    p11=p[10] 
    p17=p[16] 
    p23=p[22] 
    p29=p[28] 
     
    M = zeros((8,nn)) 
     
    M[0,:] = -1000*x1 
    M[1,:] = - 1000*x1 - 3500*d1*d2 
    M[2,:] = - 1000*x3 - 3500*p2*u2*x2 
    M[3,:] = -1000*x3 
    M[4,:] = - 2000*x4 - 3500*p11*u11*x2 
    M[5,:] = - 2000*x5 - 3500*p17*u17*x2 
    M[6,:] = - 2000*x6 - 3500*p23*u23*x2 
    M[7,:] = - 2000*x7 - 3500*p29*u29*x2 
    
    M = reshape(M,(8*nn,1),order='F')
     
    return M 
